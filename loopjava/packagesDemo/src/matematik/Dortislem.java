package matematik;

public class Dortislem {
    public static int topla(int x, int y)
    {
        return x+y;
    }
    public static int cikar(int x, int y)
    {
        return x-y;
    }
    public static int carp(int x, int y)
    {
        return x*y;
    }
    public static int bol(int x, int y)
    {
        return x/y;
    }
}